const ints = ["bigint"]; // Go int/int8/.../int64 → JS bigint
const floats = ["number"]; // Go float32/float64 → JS number

function cases(op) {
  const types = [...ints, ...floats];
  let out = "";
  for (const a of types) {
    out += `    case "${a}":\n`;
    out += `      switch (typeof b) {\n`;
    for (const b of types) {
      let t = "bigint";
      if (a === "number" || b === "number") t = "number";
      out += `        case "${b}":\n`;
      if (op === "/") {
        out += `          return Number(a) / Number(b);\n`;
      } else {
        if (t === "number") {
          out += `          return Number(a) ${op} Number(b);\n`;
        } else {
          out += `          return a ${op} b;\n`;
        }
      }
    }
    out += `      }\n`;
    out += `      break;\n`;
  }
  return out;
}

function casesIntOnly(op) {
  let out = "";
  out += `    case "bigint":\n`;
  out += `      if (typeof b === "bigint") {\n`;
  out += `        return a ${op} b;\n`;
  out += `      }\n`;
  out += `      break;\n`;
  return out;
}

function casesWithDuration(op) {
  // Same as cases but adds GoDuration handling
  const base = cases(op);
  return base; // Duration cases are handled inline in the template
}

